function prepareOptions(itemType) {
  const optionFunctions = {
    equipment: prepareEquipmentOptions,
    hybrid: prepareHybridOptions,
    passif: preparePassifOptions,
    proficiency: prepareProficiencyOptions,
    skill: prepareSkillOptions,
    spell: prepareSpellOptions,
    transformation: prepareTransformationOptions,
    weapon: prepareWeaponOptions,
  };

  const func = optionFunctions[itemType.toLowerCase()];
  return func ? func() : [];
}

